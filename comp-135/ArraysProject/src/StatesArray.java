public class StatesArray {
    public static void main(String[] args) {

        String[] states = {"California", "New York", "Iowa"};
        System.out.println(states [1]);
        System.out.println(states [2]);

        states[0] = "Utah";
        states[1] = "New Hampshire";
        states[2] = "Montana";

        System.out.println(states[0].toUpperCase());
        System.out.println(states[0]);

    }
}
